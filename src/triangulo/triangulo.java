    private void calcularMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_calcularMouseClicked
        // TODO add your handling code here:
        //creamos las varaibles a usar
        String tipo;
        double numa, numb, numc, s, area, numres, angulo, angulo_a, angulo_b, angulo_c;

        //parseamos los valores ingresados
        numa = Integer.parseInt(ladoa.getText());
        numb = Integer.parseInt(ladob.getText());
        numc = Integer.parseInt(ladoc.getText());

        //realizamos las operaciones
        //numres= ‚àö( S*(S-A) * (S-B) * (S-C) )
        //S=(A+B+C)/2
        if ((numa + numb) > numc && (numa + numc) > numb && (numb + numc) > numa) {

            s = (numa + numb + numc) / 2;
            area = Math.sqrt((s * (s - numa) * (s - numb) * (s - numc)));

            //determinamos el tipo de angulo
            if (numa == numb && numa == numc && numb == numc) {
                tipo = ("EQUILATERO ");
            } else if (numa == numb || numa == numc || numb == numc) {
                tipo = ("IS√ìSCELES");
            } else {
                tipo = ("ESCALENO");
            }
            //operacion matematica para encontrar los angulos.

            angulo_a = (Math.pow(numb, 2) + Math.pow(numc, 2) - Math.pow(numa, 2)) / (2 * numb * numc);
            angulo_b = (Math.pow(numa, 2) + Math.pow(numc, 2) - Math.pow(numb, 2)) / (2 * numa * numc);
            angulo_c = (Math.pow(numa, 2) + Math.pow(numb, 2) - Math.pow(numc, 2)) / (2 * numa * numb);

            resultado.setText(("El area del triangulo solicitado es: " +  Math.round (area)) + ("  y el tipo de triangulo es: " + tipo));
            resultadoangulos.setText(("El angulo A es: " + Math.round(Math.toDegrees(Math.acos(angulo_a)))) + (", el angulo B es: " + Math.round(Math.toDegrees(Math.acos(angulo_b))))
                    + (" y el angulo C es: " + Math.round(Math.toDegrees(Math.acos(angulo_c)))));
            ladoa.setText(""); //se limpia los jtext apenas se realizan los calulos
            ladob.setText(""); //se limpia los jtext apenas se realizan los calulos
            ladoc.setText(""); //se limpia los jtext apenas se realizan los calulos
        }

    }//GEN-LAST:event_calcularMouseClicked

